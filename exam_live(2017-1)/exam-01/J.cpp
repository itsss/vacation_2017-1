/*
사용자로부터 주민등록번호 앞 2자리와 7번째 자리를 따로 입력 받아, 한국 나이를 출력하는 프로그램을 작성하시오.

1900년생의 경우 7번째 자리가 1, 2 이지만,
2000년생의 경우 7번째 자리가 3, 4 이다.

2017년 기준으로 문제를 해결하자.
*/

#include <stdio.h>

int main()
{
	int s, num;
	scanf("%d %d", &num, &s);
	if(s == 1 || s == 2) printf("%d", (100-num) + 18);
	else printf("%d", num+18);
}
